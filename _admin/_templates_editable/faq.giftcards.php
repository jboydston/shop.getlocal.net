FAQ for Gift Cards
<p>
	<strong>How does the [giftcard_program] program work?</strong><br />
	<br />
	We are partnering with area restaurants / merchants to give you a terrific chance to try a new or favorite establishment for half price. Each week an area business will be featured, and you will have the opportunity to purchase, for example, a $50 gift Certificate for just $25 (plus processing fee) online through the [giftcard_program] website. Your Certificates are mailed directly to you.</p>
<p>
	<strong>Why is there a processing fee?</strong><br />
	<br />
	The processing fee covers the cost of completing your order. This includes delivery, transaction processing, and fulfillment.</p>
<p>
	<strong>Is buying online through El Dorado Marketplace safe?</strong><br />
	<br />
	The El Dorado Marketplace website uses secure socket layer (SSL) encryption technology to secure communications by encrypting all data to and from the site.</p>
<p>
	<strong>How long will it take to get the Certificates?</strong><br />
	<br />
	The Certificates are sent first class mail and should be received within 7 - 10 days.&nbsp; The certificates arrive in a white #10 business envelope.&nbsp; If the certificates are Egifts, then the certificate is sent immediately after purchase to your email address.&nbsp; The email will explain how to print the certificate.</p>
<p>
	<strong>What if I do not receive my Certificate?</strong><br />
	<br />
	Please contact Mountain Democrat at 530-622-1255 or marketplace@mtdemocrat.net</p>
<p>
	<strong>Can I use the Certificates immediately?</strong><br />
	<br />
	Certificates can be used as soon as they are received.</p>
<p>
	<strong>What do I do with the e-mail confirmation?</strong><br />
	<br />
	Save the confirmation for your records. You should receive one confirmation for each purchase.<br />
	<br />
	<strong>Can I purchase Certificates every week?</strong><br />
	<br />
	Yes, you can purchase one Certificate for each restaurant / merchant.</p>
<p>
	<strong>When can I purchase the Certificate?</strong><br />
	<br />
	The online sale will stay open until all the Certificate are sold.</p>
<p>
	<strong>What credit cards are supported? Can I use PayPal?</strong><br />
	<br />
	Only MasterCard&reg; and Visa&reg; are accepted at this time. PayPal is not accepted at this time.</p>
<p>
	<strong>Can we redeem the Certificates for cash?</strong><br />
	<br />
	No, the Certificates are not redeemable for cash.</p>
<p>
	<strong>Can we use the Certificates for alcohol?</strong><br />
	<br />
	Generally the Certificates may not be used for alcohol - please check with the restaurant.</p>
<p>
	I<strong>f I don&#39;t use full amount of the Certificate, will I get cash back?</strong><br />
	<br />
	Restaurants / merchants will not give cash back.</p>
<p>
	<strong>Can we use more than one Certificate per table or purchase?</strong><br />
	<br />
	No, generally there is a limit of one Certificate per table or purchase. Please check with the merchant.</p>
<p>
	<strong>Can a Certificate be used for multiple visits to the restaurant / merchant?</strong><br />
	<br />
	It is up to the individual business but generally a Certificate can only be used for one visit.</p>
<p>
	<strong>What happens if the merchant closes?</strong><br />
	<br />
	Neither El Dorado Marketplace nor the Mountain Democrat is responsible for closed establishments. There are no refunds.</p>
