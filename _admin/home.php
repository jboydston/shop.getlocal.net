<h3>Getting Started</h3>

<p><img src="<?php echo IMAGEDIR;?>/photo_01.jpg" style="display:block; float:right; margin:0px 0px 15px 15px; border:1px solid #000;" />
After you install Marketplace, you typically start with either finding a business by clicking on "Search Business" in the menu bar, or you can
add your own business by clicking on "Add Business".</p>

<p>If you are looking to target a specific area in your community with promotional ideas and opportunities, you can start by selecting
"Search Street".  This will return all business located on a specific street.</p>


